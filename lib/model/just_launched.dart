class JustLaunched{
  String _img;
  String _title;
  String _price;
  String _showroom_price;
  String _city;

  JustLaunched(this._img,this._title,this._price,this._showroom_price,this._city);

  String get img=>_img;
  String get title=>_title;
  String get price=>_price;
  String get showroom_price=>_showroom_price;
  String get city=>_city;
}